void main() {
  // Declare and initialize two variables
  var x = 5; // First number
  var y = 20; // Second number

  // To store the result of the sum, we use a new variable
  var sum; // Declare the variable for the sum
  sum = x + y; // Perform addition and store the result in 'sum'

  // Print the values of x, y, and the sum
  print(x); // Output: 5
  print(y); // Output: 20
  print(sum); // Output: 25
}
